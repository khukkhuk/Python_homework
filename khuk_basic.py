class BasicMath:
    def __init__(self):
        v=1;
    def cal_plus(self,v1 , v2,v3):
        return print(v1,"+",v2,"+",v3,"=",v1+v2+v3)

    def cal_minus(self,v1 , v2,v3):
        return print(v1,"-",v2,"-",v3,"=",v1-v2-v3)

    def cal_mul(self,v1 , v2,v3):
        return print(v1,"*",v2,"*",v3,"=",v1*v2*v3)
    
    def cal_div(self,v1 , v2,v3):
        return print(v1,"/",v2,"/",v3,"=",v1/v2/v3)
